"use strict";

const fs = require("node:fs");
const path = require("node:path");

const repoRoot = path.resolve(__dirname, "..");
const overridesPath = path.join(__dirname, "keyword-overrides.json");
const outputPath = path.join(repoRoot, "src", "data", "keywords.js");

const murexRepo =
  process.env.MUREX_REPO ||
  "/Users/laurencemorgan/dev/go/src/github.com/lmorg/murex";
const commandsDir =
  process.env.MUREX_COMMAND_DOCS_DIR || path.join(murexRepo, "docs", "commands");

function readOverrides() {
  const raw = fs.readFileSync(overridesPath, "utf8");
  const parsed = JSON.parse(raw);
  return {
    controlFlow: new Set((parsed.controlFlow || []).map((s) => s.toLowerCase())),
    includeGeneral: new Set(
      (parsed.includeGeneral || []).map((s) => s.toLowerCase())
    ),
    exclude: new Set((parsed.exclude || []).map((s) => s.toLowerCase()))
  };
}

function readCommandNames(dir) {
  const files = fs.readdirSync(dir);
  return files
    .filter((name) => name.endsWith(".md"))
    .map((name) => name.slice(0, -3).toLowerCase())
    .filter((name) => name !== "readme")
    .filter((name) => /^[a-z0-9][a-z0-9._-]*$/.test(name));
}

function toSpaceSeparated(arr) {
  return arr.join(" ");
}

function generateContent(payload) {
  return `"use strict";\n\n// AUTO-GENERATED by scripts/generate-keywords.js\n// Source: Murex docs/commands + scripts/keyword-overrides.json\n\nconst CONTROL_FLOW_KEYWORDS = ${JSON.stringify(payload.controlFlow, null, 2)};\nconst GENERAL_BUILTINS = ${JSON.stringify(payload.generalBuiltins, null, 2)};\nconst LITERALS = ${JSON.stringify(payload.literals, null, 2)};\n\nmodule.exports = {\n  CONTROL_FLOW_KEYWORDS,\n  GENERAL_BUILTINS,\n  LITERALS,\n  CONTROL_FLOW_KEYWORDS_STR: ${JSON.stringify(toSpaceSeparated(payload.controlFlow))},\n  GENERAL_BUILTINS_STR: ${JSON.stringify(toSpaceSeparated(payload.generalBuiltins))},\n  LITERALS_STR: ${JSON.stringify(toSpaceSeparated(payload.literals))}\n};\n`;
}

function main() {
  if (!fs.existsSync(commandsDir)) {
    console.error(`Commands directory not found: ${commandsDir}`);
    process.exit(1);
  }

  const overrides = readOverrides();
  const discovered = new Set(readCommandNames(commandsDir));

  for (const name of overrides.includeGeneral) discovered.add(name);
  for (const name of overrides.exclude) discovered.delete(name);

  const controlFlow = Array.from(overrides.controlFlow).sort();
  const generalBuiltins = Array.from(discovered)
    .filter((name) => !overrides.controlFlow.has(name))
    .sort();

  const payload = {
    controlFlow,
    generalBuiltins,
    literals: ["true", "false", "null"]
  };

  const content = generateContent(payload);
  fs.writeFileSync(outputPath, content, "utf8");

  console.log(`Generated ${outputPath}`);
  console.log(`Control-flow keywords: ${payload.controlFlow.length}`);
  console.log(`General builtins: ${payload.generalBuiltins.length}`);
}

main();
